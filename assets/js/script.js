// Change navbar background color when scrolling
window.addEventListener("scroll", function () {
  var navbar = document.getElementById("navbar");
  if (window.scrollY > 0) {
    navbar.classList.add("bg-custom-dark");
  } else {
    navbar.classList.remove("bg-custom-dark");
  }
});

